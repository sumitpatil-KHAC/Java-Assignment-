// 11. Print numbers from 1 to 50 using: 
// for loop 
// while loop 
class Assignment11{
	public static void main(String[] args){
		for(int i = 0 ; i <= 50 ; i++)
			System.out.print(i+ "  ");
		System.out.println("------------------");
		int a = 1;
		while(a <= 50){
			System.out.print(a+ "  ");			
			a++;
		}
	}
}
