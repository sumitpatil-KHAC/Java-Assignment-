// 1. Write a Java program to declare variables of all primitive data types and print their  values. 

class Assignment1{
   public static void main(String[] args){
       // variable declaration
       byte b ;
	   short s;
	   char ch ;
	   int i;
	   long l;
	   float f;
	   double d;
	   boolean bool;
	   // variable assignment 
	   b = 10;
	   s = 30;
	   ch = 'S';
	   i = 40;
	   l = 50l;
	   f = 60.888f;
	   d = 70.7892;
	   bool = true;
	   // printing variables
	   System.out.println("Value of byte = "+b);
	   System.out.println("Value of short = "+s);
	   System.out.println("Value of char = "+ch);
	   System.out.println("Value of int = "+i);
	   System.out.println("Value of long = "+l);
	   System.out.println("Value of float = "+f);
	   System.out.println("Value of double = "+d);
	   System.out.println("Value of boolean = "+bool);

   }
}
